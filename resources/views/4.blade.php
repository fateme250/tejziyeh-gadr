<h5>تفسیر  آیه</h3>
<div class="tafsir">
ز امام صادق عليه السلام پرسيدند: آيا روح همان جبرئيل است؟ فرمود: جبرئيل از ملائكه است و روح برتر از ملائكه است. سپس اين آيه را تلاوت فرمودند: تَنَزَّلُ الْمَلائِكَةُ وَ الرُّوحُ‌ ..

با نزول فرشتگان بر قلب پيامبر و امام معصوم در شب قدر، ارتباط زمين با آسمان، مُلك با ملكوت و طبيعت با ماوراء طبيعت برقرار مى‌شود.<br>

روح انواعى دارد:<br>

- روحى كه منشأ تمايلات و شهوات است.<br>

- روحى كه منشأ قدرت و حركات است. اين دو روح، مشترك ميان انسان و حيوان است.<br>

- روحى كه منشأ اختيار و انديشه است، كه اين روح مختص انسان است.<br>

- روحى كه عقل محض است و جوهر وجودى فرشتگان را تشكيل مى‌دهد.<br>

- روحى كه برتر از فرشتگان است و شب قدر همراه آنان به زمين نازل مى‌شود.<br>

در روايات آمده است كه با منكران امامت، با سوره قدر احتجاج كنيد.  زيرا بر اساس اين سوره، فرشتگان هر سال در شب قدر نازل مى‌شوند. در زمان حيات پيامبر بر وجود شريف حضرتش وارد مى‌شدند. پس از آن حضرت بر چه كسانى نازل مى‌شوند؟

روشن است كه هر كسى نمى‌تواند ميزبان فرشتگان الهى باشد و بايد او همچون پيامبر، معصوم بوده و ولايت امور بشر به عهده او باشد و او كسى جز امام معصوم نيست كه در زمان ما، حضرت مهدى عليه السلام مى‌باشد و فرشتگان در شب قدر شرفياب محضرش مى‌شوند

